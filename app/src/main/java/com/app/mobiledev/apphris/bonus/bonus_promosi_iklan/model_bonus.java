package com.app.mobiledev.apphris.bonus.bonus_promosi_iklan;

public class model_bonus {


    private String id_hastag;
    private String kyano;
    private String image;
    private String ket;
    private String tgl;
    private String status;


    public String getId_hastag() {
        return id_hastag;
    }

    public void setId_hastag(String id_hastag) {
        this.id_hastag = id_hastag;
    }

    public String getKyano() {
        return kyano;
    }

    public void setKyano(String kyano) {
        this.kyano = kyano;
    }

    public String getImage() {
        return image;
    }

    public void setImage(String image) {
        this.image = image;
    }

    public String getKet() {
        return ket;
    }

    public void setKet(String ket) {
        this.ket = ket;
    }

    public String getTgl() {
        return tgl;
    }

    public void setTgl(String tgl) {
        this.tgl = tgl;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

}
